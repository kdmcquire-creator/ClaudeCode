export function seedRules(db: Database.Database): void {
  const count = (db.prepare('SELECT COUNT(*) as c FROM rules').get() as { c: number }).c
  if (count > 0) return // already seeded

  const insert = db.prepare(`
    INSERT INTO rules (
      id, rule_name, section, match_type, match_value,
      account_mask_filter, amount_min, amount_max,
      day_of_week_filter, date_from_filter, date_to_filter,
      bucket, p10_category, llc_category, description_notes,
      flag_reason, action, priority_order, is_active, notes,
      created_at, updated_at
    ) VALUES (
      ?, ?, ?, ?, ?,
      ?, ?, ?,
      ?, ?, ?,
      ?, ?, ?, ?,
      ?, ?, ?, 1, ?,
      datetime('now'), datetime('now')
    )
  `)

  const insertMany = db.transaction((rules: RuleSeed[]) => {
    for (const r of rules) {
      insert.run(
        uuidv4(), r.rule_name, r.section, r.match_type, r.match_value,
        r.account_mask_filter ?? null, r.amount_min ?? null, r.amount_max ?? null,
        r.day_of_week_filter ?? null, r.date_from_filter ?? null, r.date_to_filter ?? null,
        r.bucket ?? null, r.p10_category ?? null, r.llc_category ?? null, r.description_notes ?? null,
        r.flag_reason ?? null, r.action, r.priority_order, r.notes ?? null
      )
    }
  })

  insertMany(RULES)
  console.log(`[DB] Seeded ${RULES.length} classification rules`)
}
